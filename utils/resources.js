export const getSortedCountryOptions = (locale) => {
  const countryOptions = Object.keys(countries).map((code) => ({
    code,
    name: i18nCountries.getName(code, locale),
    flag: countries[code].emoji
  }))
  return sortBy(countryOptions, ['name'])
}
